    private void studentsToNavigationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentsToNavigationButtonActionPerformed
        AdminStudentRecords adminClass = new AdminStudentRecords();
        String confirmationPrompt = "Are you sure you want to return to navigation?";
        boolean confirmation = adminClass.showConfirmationPopup(confirmationPrompt);
        if(confirmation){
            Navigation.instance.setVisible(true);
            instance.setVisible(false);
        }
        else{
            System.out.println("Staying on current page.");
        }
    }//GEN-LAST:event_studentsToNavigationButtonActionPerformed

