    private void UpdateRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateRecordButtonActionPerformed
        AdminStudentRecords functions = new AdminStudentRecords();
        String studentName = studentNameField.getText();
        String roomID = currentlyOccupyingField.getText();
        String dateOfBirth = currentlyOccupyingField.getText();
        String startDate = startDateField.getText();
        String endDate = endDateField.getText();
        functions.setName(studentName);
        String[] newDetails = {studentName, roomID, dateOfBirth, startDate, endDate};
        //order of details = name, date of birth, roomid, start date, end date
        functions.updateRecord(newDetails);
    }//GEN-LAST:event_UpdateRecordButtonActionPerformed

